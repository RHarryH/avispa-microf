package com.avispa.ecm.util;

import org.junit.jupiter.api.Test;

import java.math.BigDecimal;

import static org.junit.jupiter.api.Assertions.assertEquals;
import static org.junit.jupiter.api.Assertions.assertNotSame;

/**
 * @author Rafał Hiszpański
 */
class BigDecimalUtilsTest {
    @Test
    void givenBigDecimal_whenClone_thenValueTheSameButInstanceNot() {
        BigDecimal bigDecimal = new BigDecimal("12.04");
        BigDecimal clone = BigDecimalUtils.clone(bigDecimal);

        assertNotSame(clone, bigDecimal);
        assertEquals(clone, bigDecimal);
    }
}